    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //return $this->validation($request);
            
        $validator = Validator::make($request->input(),[
            'name' => "required", 
            // "img" => "required"
        ]);

        if($validator->fails())
        return ['success' => false,"errors" => $validator->errors()];
    
        $insert = $request->only("name");
        $insert['img'] = time().".".$request->file('img')->getClientOriginalExtension();
        $request->file('img')->move(public_path('uploads'),$insert['img']);

        $create = Book::create($insert);

        if($create)
          return ["success"=>true,'book'=>$create];
     
    }

